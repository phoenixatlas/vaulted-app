type Ctx = { lang: Lang; setLang: (l: Lang) => void; t: (k: keyof typeof dict.en) => string };
const I18nContext = createContext<Ctx>({ lang: "en", setLang: () => {}, t: (k) => dict.en[k] });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");
  useEffect(() => {
    AsyncStorage.getItem("vaulted_lang").then((v) => {
      if (v === "en" || v === "es" || v === "fr" || v === "ar") setLangState(v);
    });
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    AsyncStorage.setItem("vaulted_lang", l);
  };
  const t = (k: keyof typeof dict.en) => dict[lang][k] ?? dict.en[k];
  return React.createElement(I18nContext.Provider, { value: { lang, setLang, t } }, children);
}

export const useI18n = () => useContext(I18nContext);
